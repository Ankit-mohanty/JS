
// In case of Number
const month =3;
switch (month) {
    case 1:
        console.log("Jan");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("Mar");
        break;
    case 4:
        console.log("Apr");
        break;
    case 5:
        console.log("May");
        break;

    default:
        break;
}

// In case of String
const month1 ="jan";
switch (month1) {
    case "may":
        console.log("May Month");
        break;
    case "April":
        console.log("April Month");
        break;
    case "jan":
        console.log("January Month");
        break;

    default:
        break;
}